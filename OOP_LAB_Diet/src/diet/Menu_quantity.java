package diet;

public class Menu_quantity {

	private String menu;
	private int quantity;
	public Menu_quantity(String menu, int quantity) {
		this.menu = menu;
		this.quantity = quantity;
	}
	public String getMenu() {
		return menu;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	
	
}
